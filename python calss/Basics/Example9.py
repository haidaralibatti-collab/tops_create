# Even and Odd check using division and modulus
print(f"{4/2}  -- {4%2}")
print(f"{6/2}  -- {6%2}")
print(f"{667/2}  -- {667%2}")
# Exponentiation and floor division examples
print(f"{2**4}")
print(f"{3**4}")
# Division vs Floor Division 
print(f"{12/4} -- {12//4}")
print(f"{100/3} -- {100//3}")